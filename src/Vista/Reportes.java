    private void llenarTabla()
    {
        ArrayList<PlantelDto>lista=C.reporte3();
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[]encabezado=new String[]{"Número", "Nombre", "División", "Edad"};
        modelo.setColumnIdentifiers(encabezado);
        
        for (PlantelDto P : lista) {
            
            Object[]filas=new Object[]{P.getNumero(), P.getNombre(),P.getNombreD(),P.getEdad()};
            modelo.addRow(filas);
        }
        tblLista.setModel(modelo);
    }

