     private void cargarComboP()
    {
        ArrayList<Posicion>lista=C.llenarComboP();
        DefaultComboBoxModel modelo=new DefaultComboBoxModel();
        modelo.addElement("Seleccione");
        
        for (Posicion P : lista) {
            modelo.addElement(P.getNombreP());
        }
        cboProsicion.setModel(modelo);
    }
     private void cargarComboD()
    {
        ArrayList<Division>lista=C.llenarComboD();
        DefaultComboBoxModel modelo=new DefaultComboBoxModel();
        modelo.addElement("Seleccione");
        
        for (Division D : lista) {
            modelo.addElement(D.getNombreD());
        }
        cboDivision.setModel(modelo);
    }
     
     private void limpiarCampos()
     {
         txtNumero.setText("");
         txtNombre.setText("");
         txtEdad.setText("");
         cboProsicion.setSelectedIndex(0);
         cboDivision.setSelectedIndex(0);
     }

